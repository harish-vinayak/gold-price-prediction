# -*- coding: utf-8 -*-
"""Gold Price Prediction App — With Calendar Features"""

# ============================================================
# Import Libraries
# ============================================================
import streamlit as st
import numpy as np
import pandas as pd
import tensorflow as tf
import joblib
from sklearn.preprocessing import MinMaxScaler
import matplotlib.pyplot as plt

import joblib

# Load the saved scaler
scaler = joblib.load("scaler.save")


# ============================================================
# Streamlit Config (MUST BE FIRST Streamlit Command)
# ============================================================
try:
    st.set_page_config(page_title="Gold Price Predictor", page_icon="💰", layout="centered")
except Exception:
    pass

# ============================================================
# Title & Info
# ============================================================
st.title("💰 Gold Price Prediction (22K & 24K)")
st.write(
    "This app predicts tomorrow’s **Gold Price** using a trained LSTM model with calendar features. "
    "It also compares **Actual vs Predicted** gold prices with a smooth curve."
)

# ============================================================
# Load Trained Model and Scaler
# ============================================================
@st.cache_resource
def load_model():
    return tf.keras.models.load_model("gold_model_with_calendar.keras")

@st.cache_resource
def load_scaler():
    return joblib.load("scaler_with_calendar.save")

model = load_model()
scaler = load_scaler()

# ============================================================
# File Upload Section
# ============================================================
uploaded_file = st.file_uploader("📂 Upload dataset (CSV file with Date, Gold_INR_g, Silver_INR_g, USDINR columns)", type=["csv"])

if uploaded_file is not None:
    df = pd.read_csv(uploaded_file)

    # Convert Date to datetime
    if "Date" in df.columns:
        df["Date"] = pd.to_datetime(df["Date"])
    else:
        st.error("❌ Dataset must contain a 'Date' column.")
        st.stop()

    # Add calendar features
    df["day_of_week"] = df["Date"].dt.dayofweek
    df["month"] = df["Date"].dt.month

    st.subheader("📊 Data Preview")
    st.dataframe(df.tail())

    # ============================================================
    # Data Preparation
    # ============================================================
    features = ["Gold_INR_g", "Silver_INR_g", "USDINR", "day_of_week", "month"]
    df = df.dropna(subset=features)
    df_feat = df[features]

    # Scale features using loaded scaler
    scaled = scaler.transform(df_feat)

    # Separate scaler for Gold only (use MinMaxScaler fitted on Gold price)
    gold_scaler = MinMaxScaler()
    gold_scaler.min_, gold_scaler.scale_ = scaler.min_[0], scaler.scale_[0]  # use gold scaling from main scaler

    # Parameters
    history_len = 100
    window_size = 60
    future_days = 7

    if len(scaled) < window_size:
        st.error("❌ Not enough data to make predictions (need at least 60 days).")
    else:
        # Actual last 100 prices
        y_true = df_feat["Gold_INR_g"].values[-history_len:]

        # Predict on history (last ~40 days within last 100)
        pred_on_hist = []
        start_idx = len(scaled) - history_len
        end_idx = len(scaled) - window_size + 1
        for i in range(start_idx, end_idx):
            window_input = scaled[i : i + window_size]
            window_input = np.expand_dims(window_input, axis=0)
            pred_scaled = model.predict(window_input, verbose=0)
            pred_on_hist.append(pred_scaled[0, 0])

        # Inverse scale predictions
        pred_on_hist = gold_scaler.inverse_transform(np.array(pred_on_hist).reshape(-1, 1)).flatten()

        # Predict future days
        X_input = scaled[-window_size:]
        X_input = np.expand_dims(X_input, axis=0)
        future_preds = []
        current_input = X_input.copy()

        for _ in range(future_days):
            pred_scaled = model.predict(current_input, verbose=0)
            future_preds.append(pred_scaled[0, 0])

            # Create next input window row with predicted Gold price and same Silver, USDINR, day_of_week, month as last row
            new_row = np.append(
                current_input[0, 1:, :],
                np.array(
                    [
                        [
                            pred_scaled[0, 0],                    # Predicted Gold price scaled
                            current_input[0, -1, 1],             # Silver_INR_g scaled (same as last)
                            current_input[0, -1, 2],             # USDINR scaled (same as last)
                            current_input[0, -1, 3],             # day_of_week (same as last)
                            current_input[0, -1, 4],             # month (same as last)
                        ]
                    ]
                ),
                axis=0,
            )
            current_input = np.expand_dims(new_row, axis=0)

        future_preds = gold_scaler.inverse_transform(np.array(future_preds).reshape(-1, 1)).flatten()

        # Display predicted prices for tomorrow
        price_24k = future_preds[0]
        price_22k = price_24k * 0.916
        st.success(f"📈 **Predicted 24K Gold Price for Tomorrow:** ₹{price_24k:.2f}/gram")
        st.info(f"💛 **Predicted 22K Gold Price for Tomorrow:** ₹{price_22k:.2f}/gram")

        # Plot actual vs predicted
        x_actual = range(history_len)
        x_pred_hist = range(history_len - len(pred_on_hist), history_len)
        x_pred_future = range(history_len, history_len + future_days)

        fig, ax = plt.subplots(figsize=(10, 5))
        ax.plot(x_actual, y_true, label="Actual Gold Price", color="royalblue", linewidth=2)
        ax.plot(x_pred_hist, pred_on_hist, label="Predicted on History", color="darkorange", linestyle="dashed", linewidth=2)
        ax.plot(x_pred_future, future_preds, label="Future Forecast", color="darkorange", linestyle="dashed", linewidth=2)

        ax.set_title("Gold Price Prediction — Actual vs Predicted & Forecast", fontsize=14)
        ax.set_xlabel("Days", fontsize=12)
        ax.set_ylabel("INR/gram", fontsize=12)
        ax.legend()
        ax.grid(True, linestyle="--", alpha=0.6)

        st.pyplot(fig)

        st.caption(
            "🧠 Model uses a 60-day input window with calendar features to predict the next 7 days. "
            "This helps smooth out sharp dips and improves accuracy."
        )

else:
    st.info("⬆️ Please upload a dataset CSV file to begin prediction.")

st.subheader("📈 Year-wise Gold Price Trend")
year_avg = df.groupby(df['Date'].dt.year)['Gold_INR_g'].mean()
fig, ax = plt.subplots()
year_avg.plot(marker='o', color='orange', ax=ax)
ax.set_title("Year-wise Average Gold Price")
ax.set_xlabel("Year")
ax.set_ylabel("INR/gram")
st.pyplot(fig)

import seaborn as sns

st.subheader("📉 Feature Correlation Heatmap")
corr = df[["Gold_INR_g","Silver_INR_g","USDINR","day_of_week","month"]].corr()
fig, ax = plt.subplots()
sns.heatmap(corr, annot=True, cmap="YlOrBr", ax=ax)
ax.set_title("Correlation between Features")
st.pyplot(fig)

st.subheader("🔍 Predicted vs Actual (Last 30 Days)")
fig, ax = plt.subplots()
ax.plot(y_true[-30:], label="Actual Gold", color="blue")
ax.plot(pred_on_hist[-30:], label="Predicted Gold", color="orange", linestyle="dashed")
ax.set_title("Actual vs Predicted Gold Price (30 Days)")
ax.legend()
st.pyplot(fig)

st.subheader("🪙 Gold vs Silver Price Comparison (Dual Y-Axis)")

fig, ax1 = plt.subplots(figsize=(10,5))

# Plot Gold on left axis
ax1.plot(df["Date"], df["Gold_INR_g"], color="gold", label="Gold (INR/g)")
ax1.set_xlabel("Date")
ax1.set_ylabel("Gold Price (INR/gram)", color="gold")
ax1.tick_params(axis='y', labelcolor="gold")

# Plot Silver on right axis
ax2 = ax1.twinx()
ax2.plot(df["Date"], df["Silver_INR_g"], color="silver", label="Silver (INR/g)")
ax2.set_ylabel("Silver Price (INR/gram)", color="gray")
ax2.tick_params(axis='y', labelcolor="gray")

# Title & Legend
fig.suptitle("Gold vs Silver Price Trend", fontsize=14)
fig.tight_layout()
st.pyplot(fig)